#Take input values from the user
x = 63
y = 27
z = 15

print("Before Swapping:")
print("x =", x)
print("y =", y)
print("z =", z)

print()

#Swapping
temp = x
x = y
y = z
z = temp

#Displaying the result after swapping
print("Value of x after swapping:", x)
print("Value of y after swapping:", y)
print("Value of z after swapping:", z)